# 28、列表的创建(列表是可以变化的)
a = list()              # list()括号可以放list变量，不放则表示是空列表
b = [1, 2, 3, 4, 5]     # []表示一个列表，列表里面可以放不同元素的类型
c = [1, "666", 10.5]
print(type(a))
print(a)
print(b)
print(c)

# 29、可以通过下标访问操作符[]来获取到列表中的任意元素
# a、注意下标是从0开始的
print(c[0], end = " ")
print(c[1], end = " ")
print(c[2])
# b、可以通过下标修改元素
c[0] = "赵"
print(c[0])
# c、如果下标超出列表的有效范围, 会抛出异常
# d、负数下标
# 负数表示倒数第几个元素
print(c[-1], end = " ")
print(c[-2], end = " ")
print(c[-3], end = " ")